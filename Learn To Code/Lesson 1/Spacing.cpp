#include <iostream>
using namespace std;

int main ()
{
	cout << "Hello World! \n";
	// prints Hello World! with a space at the end
	cout << "I love Programming" << endl;
	// prints I Love Programming with a space at the end (endl = /n)
	cout << "just testing\n\n";
	// prints just testing with a double spacing at the end
	cout << "THIS \nIS \nAWESOME! \n"; 
	// prints thus is awesome with spaceing in each texts
	return 0;
}
