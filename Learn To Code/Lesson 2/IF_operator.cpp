#include <iostream>
using namespace std;

int main ()
{
	if(10>=10)
	// if 10 is bigger or equal to 10
	{
		cout << "yes" <<endl;
	}
	if(10<=10)
	// if 10 is smaller or equal to 10
	{
		cout << "yes" <<endl;
	}
	if (10==10)
	// of 10 is equal to 10
	{
		cout << "yes" << endl;
	}
	if (10!=9)
	// if 10 is not equal to 9
	{
		cout << "yes" << endl;
	}
	return 0;
}
